package com.sundar.demo.Model;

public class Banner {

    public String name;
    public String image;

    public Banner() {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage(String image) {
        return this.image;
    }

    public void setImage(String image) {
        this.image = image;
    }


}
